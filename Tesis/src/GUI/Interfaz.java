    private void generarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generarActionPerformed
        // TODO add your handling code here:
         try {
                String ruta = "Informe.txt";
                File archivo = new File(ruta);
                BufferedWriter bw;
                bw = new BufferedWriter(new FileWriter(archivo));
                bw.write("------------------------------------------------------\n");
                bw.newLine();
                bw.write("Descargar Realizadas \n");
                bw.newLine();
                bw.write("------------------------------------------------------\n");
                for (Registro registro : Estadisticas.descargas){
                  bw.newLine();
                  bw.write("Archivo: "+registro.getNombre()+" Tamaño: "+registro.getTamano() + " bytes "
                          +" Status: "+registro.getStatus()+" \n");
                  bw.newLine();
                }
                bw.write("------------------------------------------------------\n");
                bw.newLine();
                bw.close();
                System.out.println("Se ha generado el archivo con exito!");
                Controller.setLog(logs,"Se ha generado el archivo con exito!");
            } catch (IOException ex) {
                Logger.getLogger(Estadistica.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_generarActionPerformed

    private String seleccionarPuerto(){
        String respuesta ="";
        Integer valor =0;
        while ((valor<2001)||(valor>5000)){
            Random r = new Random();
            valor = r.nextInt(5000);
        }
        respuesta = valor.toString();
        return respuesta;
    }
    
    public boolean busquedaInterna(String nombre,Long hash){
      Recurso recurso = Nodo.getInstancia().buscarRecurso(nombre);
      ArrayList<Nodo> lista = new ArrayList<Nodo>();
      lista.add(Nodo.getInstancia());
      if (recurso != null) {
        new Descargas(lista,nombre,hash).start();
        return true; 
      }
      return false; 
    }
    
